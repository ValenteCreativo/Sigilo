"use client";

import { ButtonProps } from "@/types";
import { forwardRef } from "react";

const variants = {
  primary:
    "bg-sigilo-teal hover:bg-sigilo-teal-dark text-white shadow-lg shadow-sigilo-teal/20 hover:shadow-sigilo-teal/30",
  secondary:
    "bg-sigilo-card border border-sigilo-border hover:bg-sigilo-border/50 text-sigilo-text-primary",
  ghost:
    "bg-transparent hover:bg-sigilo-border/30 text-sigilo-text-secondary hover:text-sigilo-text-primary",
  danger:
    "bg-sigilo-red/10 border border-sigilo-red/30 hover:bg-sigilo-red/20 text-sigilo-red",
};

const sizes = {
  sm: "px-3 py-1.5 text-sm",
  md: "px-4 py-2 text-sm",
  lg: "px-6 py-3 text-base",
};

export const Button = forwardRef<HTMLButtonElement, ButtonProps>(
  (
    {
      variant = "primary",
      size = "md",
      isLoading = false,
      leftIcon,
      rightIcon,
      className = "",
      children,
      disabled,
      ...props
    },
    ref
  ) => {
    return (
      <button
        ref={ref}
        disabled={disabled || isLoading}
        className={`
          inline-flex items-center justify-center gap-2
          font-medium rounded-lg
          transition-all duration-200
          focus:outline-none focus:ring-2 focus:ring-sigilo-teal/50 focus:ring-offset-2 focus:ring-offset-sigilo-bg
          disabled:opacity-50 disabled:cursor-not-allowed
          ${variants[variant]}
          ${sizes[size]}
          ${className}
        `}
        {...props}
      >
        {isLoading ? (
          <svg
            className="animate-spin h-4 w-4"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
          >
            <circle
              className="opacity-25"
              cx="12"
              cy="12"
              r="10"
              stroke="currentColor"
              strokeWidth="4"
            />
            <path
              className="opacity-75"
              fill="currentColor"
              d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
            />
          </svg>
        ) : (
          leftIcon
        )}
        {children}
        {!isLoading && rightIcon}
      </button>
    );
  }
);

Button.displayName = "Button";
